            <p><em>With over 100 million repositories on GitHub, it can be overwhelming to find the right repository for yourself.<br>
But with a bit of research and some help from the GitHub community, you can easily find your way through the millions of repositories on GitHub.</em></p>

<p>Here is a curated list of GitHub repositories that will really help you to advance your career as a <strong>Developer</strong>:</p>

<h2>
  <a name="1-project-based-learning" href="#1-project-based-learning">
  </a>
  1. <a href="https://github.com/practical-tutorials/project-based-learning">Project Based Learning</a>
</h2>

<p>A list of programming tutorials in which aspiring software developers learn how to build an application from scratch. These tutorials are divided into different primary programming languages. Tutorials may involve multiple technologies and languages.</p>

<h2>
  <a name="2-devops-exercises" href="#2-devops-exercises">
  </a>
  2. <a href="https://github.com/bregman-arie/devops-exercises">Devops Exercises</a>
</h2>

<p>If you want to work as a DevOps engineer, learning some of the concepts mentioned in this repository will be beneficial.<br>
This repository Contains questions and exercises on a variety of technical DevOps and SRE topics.</p>

<h2>
  <a name="3-sre-interview-preparation-guide" href="#3-sre-interview-preparation-guide">
  </a>
  3. <a href="https://github.com/bregman-arie/devops-exercises">SRE Interview Preparation Guide</a>
</h2>

<p>This repository is an attempt to consolidate useful resources for Site Reliability Engineer (SRE) interview preparation.</p>

<h2>
  <a name="4-the-system-design-primer" href="#4-the-system-design-primer">
  </a>
  4. <a href="https://github.com/donnemartin/system-design-primer">The System Design Primer</a>
</h2>

<p>Learning how to design scalable systems will help you become a better engineer.<br>
System design is a broad topic. There is a vast amount of resources scattered throughout the web on system design principles.<br>
This repository is an organized collection of resources to help you learn how to build systems at scale.</p>

<h2>
  <a name="5-sysadmin-interview-questions" href="#5-sysadmin-interview-questions">
  </a>
  5. <a href="https://github.com/gracco/sysadmin-interview-questions">Sysadmin Interview Questions</a>
</h2>

<p>A collection of Linux System Administrator/DevOps interview questions. Feel free to contribute via pull requests, issues, or email messages.</p>

<h2>
  <a name="6-every-programmer-should-know" href="#6-every-programmer-should-know">
  </a>
  6. <a href="https://github.com/mtdvio/every-programmer-should-know">Every Programmer Should Know</a>
</h2>

<p>A collection of (mostly) technical things every software developer should know about.</p>

<h2>
  <a name="7-coding-interview-university" href="#7-coding-interview-university">
  </a>
  7. <a href="https://github.com/jwasham/coding-interview-university">Coding Interview University</a>
</h2>

<p>A complete computer science study plan to become a software engineer.</p>

<h2>
  <a name="8-developer-roadmap" href="#8-developer-roadmap">
  </a>
  8. <a href="https://github.com/kamranahmedse/developer-roadmap">Developer Roadmap</a>
</h2>

<p>Developer Roadmap is a community effort to create roadmaps, guides and other educational content to help guide the developers in picking up the path and guide their learnings.<br>
Interactive roadmaps, guides and other educational content to help developers grow in their careers.</p>

<h2>
  <a name="9-test-your-sysadmin-skills" href="#9-test-your-sysadmin-skills">
  </a>
  9. <a href="https://github.com/trimstray/test-your-sysadmin-skills">Test Your Sysadmin Skills</a>
</h2>

<p>A collection of Linux Sysadmin Test Questions and Answers. Test your knowledge and skills in different fields with these Q/A.</p>

<h2>
  <a name="10-frontend-dev-bookmarks" href="#10-frontend-dev-bookmarks">
  </a>
  10. <a href="https://github.com/dypsilon/frontend-dev-bookmarks">Frontend Dev Bookmarks</a>
</h2>

<p>Manually curated collection of resources for frontend web developers.</p>

